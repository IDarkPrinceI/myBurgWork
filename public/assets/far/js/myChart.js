$("#datePickerStart").on('change', function () {
    $("#dataPickerFinishDiv").removeClass('d-none')
})

$("#datePickerFinish").on('change', function () {
    $("#chartBuild").removeClass('d-none')
})


$("#chartBuild").on('click', function () {
    const dateStart = $("#datePickerStart").val()
    const dateFinish = $("#datePickerFinish").val()
    $.ajax({
        url: '/far/chart/' + dateStart + '/' + dateFinish,
        success: function (res) {
            const ctx = $("#myChart");
            const myChart = new Chart(ctx, {
                type: 'line',
                data: {
                    labels: ['Red', 'Blue', 'Yellow', 'Green', 'Purple', 'Orange'],
                    datasets: [{
                        label: '# of Votes',
                        data: [12, 19, res[0], res[1], 2, 5],
                        backgroundColor: [
                            'rgba(255, 99, 132, 0.2)',
                            'rgba(54, 162, 235, 0.2)',
                            'rgba(255, 206, 86, 0.2)',
                            'rgba(75, 192, 192, 0.2)',
                            'rgba(153, 102, 255, 0.2)',
                            'rgba(255, 159, 64, 0.2)'
                        ],
                        borderColor: [
                            'rgba(255, 99, 132, 1)',
                            'rgba(54, 162, 235, 1)',
                            'rgba(255, 206, 86, 1)',
                            'rgba(75, 192, 192, 1)',
                            'rgba(153, 102, 255, 1)',
                            'rgba(255, 159, 64, 1)'
                        ],
                        borderWidth: 1
                    }]
                },
                options: {
                    scales: {
                        yAxes: [{
                            ticks: {
                                beginAtZero: true
                            }
                        }]
                    }
                }
            });
            console.log(res)
            alert('Ура')
        },
        error: function () {
            alert('Ошибка')
        }
    })
    // console.log(date)
})



